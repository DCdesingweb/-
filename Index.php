    document.addEventListener("DOMContentLoaded", function () {
        const questions = document.querySelectorAll(".faq-question");
        
        questions.forEach(question => {
            question.addEventListener("click", function () {
                // Cierra cualquier respuesta abierta
                questions.forEach(q => {
                    if (q !== question) {
                        q.classList.remove("active");
                        const answer = q.nextElementSibling;
                        if (answer) answer.style.display = "none"; // Oculta la respuesta
                    }
                });
                
                // Alterna la respuesta de la pregunta actual
                this.classList.toggle("active");
                const answer = this.nextElementSibling;
                if (answer) {
                    answer.style.display = this.classList.contains("active") ? "block" : "none"; // Muestra u oculta la respuesta
                }
            });
        });
    });
</script>



